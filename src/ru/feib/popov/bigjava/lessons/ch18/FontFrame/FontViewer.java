    private void ItalicButtonClicked(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ItalicButtonClicked
        fontStyle += (this.jCheckBoxStyleItalic.isSelected()) ? 
                +Font.ITALIC :
                -Font.ITALIC;
        applyFont();
        
    }//GEN-LAST:event_ItalicButtonClicked

    private void BoldButtonClicked(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BoldButtonClicked
        fontStyle += (this.jCheckBoxStyleBold.isSelected()) ? 
                +Font.BOLD : 
                -Font.BOLD;
        applyFont();
    }//GEN-LAST:event_BoldButtonClicked
    
    private void applyFont() {
        this.jLabelText.setFont(new Font(fontName, fontStyle, fontSize));
        this.pack();
        this.repaint();
    }
    
