    private void askComponents() {
        fontName = (String)this.jComboBoxFont.getSelectedItem();
        
        if (this.jRadioButtonSizeSmall.isSelected()) {
            fontSize = SMALL_SIZE;
        } else if (this.jRadioButtonSizeMedium.isSelected()) {
            fontSize = MEDIUM_SIZE;
        } else if (this.jRadioButtonSizeLarge.isSelected()) {
            fontSize = LARGE_SIZE;
        }
        
        fontStyle = 
                ((this.jCheckBoxStyleItalic.isSelected()) ? 
                    Font.ITALIC : 0) 
                + 
                ((this.jCheckBoxStyleBold.isSelected()) ? 
                    Font.BOLD : 0);
    }

